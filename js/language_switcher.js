function switchLanguage(lang) {
    // Loop through all elements with a data-i18n attribute
    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.getAttribute("data-i18n");
        // If a translation exists for the key and language, update the HTML
        if (translations[lang] && translations[lang][key]) {
            // Insert translated HTML (allows <br/> and other tags)
            el.innerHTML = translations[lang][key];
        }
    });
}

// Add click event listeners to all language buttons and save choice
document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", function () {
        const lang = this.dataset.lang;
        switchLanguage(lang); // Switch to selected language
        localStorage.setItem("lang", lang); // Save language preference in localStorage
    });
});

// Read and apply the saved language preference when the page loads
document.addEventListener("DOMContentLoaded", function () {
    const savedLang = localStorage.getItem("lang") || "de"; // Default to German
    switchLanguage(savedLang); // Set language on page load
});

